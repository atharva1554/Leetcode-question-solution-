class Solution {
  public:
    // Function to sort the given linked list using Merge Sort.
    Node* mergeSort(Node* head) {
        // your code here
        vector<int>a;
        Node *temp=head;
        while(temp)
        {
            a.push_back(temp->data);
            temp=temp->next;
        }
        sort(a.begin(),a.end());
        temp=head;
        for(int i=0;i<a.size();i++)
        {
            temp->data=a[i];
            temp=temp->next;
        }
        return head;
        
    }
};


